// Get video details by ID
export const getVideoById = (id: string) => {
  // First check in videoDetails
  if (videoDetails[id as keyof typeof videoDetails]) {
    return videoDetails[id as keyof typeof videoDetails];
  }

  // Check in other collections
  const allVideos = [
    ...trendingVideos,
    ...documentaries,
    ...popularCreators,
    ...newReleases
  ];

  const video = allVideos.find(v => v.id === id);
  
  if (video) {
    return {
      ...video,
      description: 'This is a detailed description of the video that explains its content and context. It provides information about what viewers can expect to learn or experience when watching this video.',
      videoUrl: 'https://joy1.videvo.net/videvo_files/video/free/2019-09/large_watermarked/190828_27_SuperTrees_HD_17_preview.mp4',
      likes: Math.floor(video.views * 0.06),
      createdAt: '2023-11-18',
      tags: ['entertainment', 'education', 'technology'],
      relatedVideos: [
        trendingVideos[0].id, 
        popularCreators[0].id,
        documentaries[0].id, 
        newReleases[0].id
      ].filter(relId => relId !== id)
    };
  }

  return null;
};

// Get related videos
export const getRelatedVideos = (videoIds: string[]) => {
  const allVideos = [
    ...trendingVideos,
    ...documentaries,
    ...popularCreators,
    ...newReleases
  ];

  return videoIds
    .map(id => allVideos.find(v => v.id === id))
    .filter(video => video !== undefined) as typeof trendingVideos;
};

// Search videos
export const searchVideos = (query: string) => {
  const allVideos = [
    ...trendingVideos,
    ...documentaries,
    ...popularCreators,
    ...newReleases
  ];

  query = query.toLowerCase();
  
  return allVideos.filter(video => 
    video.title.toLowerCase().includes(query) || 
    video.creator.name.toLowerCase().includes(query)
  );
};